#Python distinguishes between 3 number types

#integer - counting numbers, positive and negitive. NO DECIMAL POINT

#Integers are created by a literal number with no decimal point or through the use of the int() constructor

print(3)
print(int(19))
print(int())

#Floating point number - decimals nums are called floating point numbers
#floats are created using numbers with a deciaml point with the float() constructor.

print(2.24)
print(2.)
print(float())
print(27e-5)

